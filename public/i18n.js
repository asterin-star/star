/**
 * Get browser language preference
 */
function getBrowserLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    const langCode = browserLang.split('-')[0]; // Get primary language code

    // Only support languages with complete translations
    const supportedLanguages = ['es', 'en', 'pt', 'fr', 'de', 'ja', 'ko', 'zh'];

    if (supportedLanguages.includes(langCode)) {
        return langCode;
    }

    // Default to Spanish
    return 'es';
}

/**
 * Get translation for a key
 */
function t(key, ...args) {
    const lang = currentLanguage || 'es';
    let text = translations[lang][key] || translations['es'][key] || key;

    // Replace placeholders {0}, {1}, etc.
    args.forEach((arg, index) => {
        text = text.replace(`{${index}}`, arg);
    });

    return text;
}

/**
 * Apply translations to the page
 */
function applyTranslations() {
    // Update document title
    document.title = t('title');

    // Update meta description
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) {
        metaDesc.setAttribute('content', t('metaDescription'));
    }

    // Update section titles
    const titleArquetipo = document.getElementById('titleArquetipo');
    const titleMisticismo = document.getElementById('titleMisticismo');
    const titleSombra = document.getElementById('titleSombra');
    const titleBotanica = document.getElementById('titleBotanica');
    const titleCotidiano = document.getElementById('titleCotidiano');
    const titleGnosis = document.getElementById('titleGnosis');
    const titleResonanciaBiblica = document.getElementById('titleResonanciaBiblica');

    if (titleArquetipo) titleArquetipo.textContent = t('shadowsTitle');
    if (titleMisticismo) titleMisticismo.textContent = t('mysticismTitle');
    if (titleSombra) titleSombra.textContent = t('shadowTitle');
    if (titleBotanica) titleBotanica.textContent = t('botanicTitle');
    if (titleCotidiano) titleCotidiano.textContent = t('dailyTitle');
    if (titleGnosis) titleGnosis.textContent = t('gnosisTitle');
    if (titleResonanciaBiblica) titleResonanciaBiblica.textContent = t('biblicalTitle');

    // Update AI button label
    const aiBtn = document.getElementById('aiBtn');
    if (aiBtn) {
        aiBtn.setAttribute('aria-label', t('aiButtonLabel'));
    }

    // Update card accessibility label
    const cardStage = document.getElementById('cardStage');
    if (cardStage && !cardStage.classList.contains('flipped')) {
        cardStage.setAttribute('aria-label', t('revealCard'));
    }

    // Update Patreon link
    const patreonBtn = document.querySelector('.patreon-btn');
    if (patreonBtn) {
        patreonBtn.setAttribute('aria-label', t('patreonLabel'));
    }

    // Update card image alt
    const cardImg = document.getElementById('cardImg');
    if (cardImg) {
        cardImg.setAttribute('alt', t('cardAlt'));
    }

    // Update AI header
    const aiHeaderElements = document.querySelectorAll('.ai-header');
    aiHeaderElements.forEach(el => {
        const iconSpan = el.querySelector('.ai-icon-small');
        if (iconSpan) {
            el.innerHTML = `<span class="ai-icon-small">âœ¦</span> ${t('numerologyHeader')}`;
        }
    });

    // Update Q&A section elements
    const qaTitleElements = document.querySelectorAll('[data-i18n="qaTitle"]');
    qaTitleElements.forEach(el => el.textContent = t('qaTitle'));

    const qaPlaceholderElements = document.querySelectorAll('[data-i18n-placeholder="qaPlaceholder"]');
    qaPlaceholderElements.forEach(el => el.placeholder = t('qaPlaceholder'));

    const qaSubmitElements = document.querySelectorAll('[data-i18n="qaSubmitButton"]');
    qaSubmitElements.forEach(el => el.textContent = t('qaSubmitButton'));

    const qaAnswerHeaderElements = document.querySelectorAll('[data-i18n="qaAnswerHeader"]');
    qaAnswerHeaderElements.forEach(el => el.textContent = t('qaAnswerHeader'));
}

